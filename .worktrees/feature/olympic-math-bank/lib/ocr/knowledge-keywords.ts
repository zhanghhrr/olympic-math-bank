/**
 * 获取标签的完整路径（五级）
 */
export function getTagPath(tag: any): string {
  const parts: string[] = [];

  if (tag.parent?.parent?.parent?.parent) {
    parts.push(tag.parent.parent.parent.parent.name);
  }
  if (tag.parent?.parent?.parent) {
    parts.push(tag.parent.parent.parent.name);
  }
  if (tag.parent?.parent) {
    parts.push(tag.parent.parent.name);
  }
  if (tag.parent) {
    parts.push(tag.parent.name);
  }
  parts.push(tag.name);

  return parts.join(' > ');
}

/**
 * 获取标签的层级展示（用于UI显示）
 */
export function getTagHierarchy(tag: any): {
  level1?: string;
  level2?: string;
  level3?: string;
  level4?: string;
  level5: string;
} {
  return {
    level1: tag.parent?.parent?.parent?.parent?.name,
    level2: tag.parent?.parent?.parent?.name,
    level3: tag.parent?.parent?.name,
    level4: tag.parent?.name,
    level5: tag.name,
  };
}

/**
 * 获取标签的完整路径（五级），用短线连接
 */
export function getTagPathDash(tag: any): string {
  const hierarchy = getTagHierarchy(tag);
  const parts = [
    hierarchy.level1,
    hierarchy.level2,
    hierarchy.level3,
    hierarchy.level4,
    hierarchy.level5,
  ].filter(Boolean);

  return parts.join(' - ');
}
