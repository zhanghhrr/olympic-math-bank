/**
 * 获取标签的完整路径（五级）
 */
export function getTagPath(tag: any): string {
  const parts: string[] = [];

  if (tag.parent?.parent?.parent?.parent) {
    parts.push(tag.parent.parent.parent.parent.name); // 一级：模块
  }
  if (tag.parent?.parent?.parent) {
    parts.push(tag.parent.parent.parent.name); // 二级：专题
  }
  if (tag.parent?.parent) {
    parts.push(tag.parent.parent.name); // 三级：子专题
  }
  if (tag.parent) {
    parts.push(tag.parent.name); // 四级：知识点
  }
  parts.push(tag.name); // 五级：技能

  return parts.join(' > ');
}

